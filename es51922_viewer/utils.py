import os
import sys

import pkg_resources


def getResource(resource):
    if not hasattr(sys, 'frozen'):
        return pkg_resources.resource_filename('es51922_viewer.gui', resource)
    else:
        return os.path.join(sys._MEIPASS, 'gui', resource)
